public void llenarG(){

    try {
        ControlGrupo cg = new ControlGrupo();
        lg = cg.mostrar();
        Grupo[] g = new Grupo[lg.size()];
        for (int i = 0; i < lg.size(); i++) {
            g[i] = lg.get(i);
        }
        DefaultComboBoxModel dc= new DefaultComboBoxModel(g);
        this.jComboBox1.setModel(dc);
    } catch (Exception e) {
        e.printStackTrace();
    }
}

public void tableE(){
String[] columnas = {"materia","hora","dia"};
Object[] obj = new Object[3];
DefaultTableModel dtm = new DefaultTableModel(null,columnas);
ControlHorario ch = new ControlHorario();
Grupo g = (Grupo) jComboBox1.getSelectedItem();
List<Horario> lh = ch.mostrar(g.getIdGrupo());
    for (Horario h : lh) {
        obj[0] = h.getMateria();
        obj[1] = h.getDia();
        obj[2] = h.getHora();
        dtm.addRow(obj);
    }
    jTable1.setModel(dtm);
}
